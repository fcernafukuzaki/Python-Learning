import numpy as np

# Estadística
A = np.arange(10)

# Promedio
print('P R O M E D I O')
print(np.mean(A))

# Mediana
print('M E D I A N A')
print(np.median(A))

# Percentil
print('P E R C E N T I L')
print(np.percentile(A, 40))

# Generar números random
print('R A N D O M')
print(np.random.random(10))
